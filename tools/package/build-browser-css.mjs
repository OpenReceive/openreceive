#!/usr/bin/env node

import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { spawnSync } from "node:child_process";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../..");
const browserRoot = path.join(root, "packages/js/browser");
const input = path.join(browserRoot, "src/styles.source.css");
const distCss = path.join(browserRoot, "dist/styles.css");
const srcCss = path.join(browserRoot, "src/styles.css");
const generatedDir = path.join(browserRoot, "src/generated");
const generatedTs = path.join(generatedDir, "compiled-styles.ts");

mkdirSync(path.dirname(distCss), { recursive: true });
mkdirSync(generatedDir, { recursive: true });

const cli = path.join(root, "node_modules/@tailwindcss/cli/dist/index.mjs");
const result = spawnSync(
  process.execPath,
  [cli, "-i", input, "-o", distCss, "--minify"],
  { cwd: browserRoot, encoding: "utf8" },
);

if (result.status !== 0) {
  process.stderr.write(result.stdout ?? "");
  process.stderr.write(result.stderr ?? "");
  process.exit(result.status ?? 1);
}

const css = readFileSync(distCss, "utf8");
writeFileSync(srcCss, css);
writeFileSync(
  generatedTs,
  `/* Generated by tools/package/build-browser-css.mjs — do not edit. */\nexport const openReceiveCompiledStyles = ${JSON.stringify(css)};\n`,
);

process.stdout.write(`built browser css (${css.length} bytes)\n`);
