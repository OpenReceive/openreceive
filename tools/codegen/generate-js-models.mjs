#!/usr/bin/env node

import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { parse as parseYaml } from "yaml";

const root = process.cwd();
const outputPath = "packages/js/core/src/generated/contracts.ts";
const check = process.argv.includes("--check");
const EMPTY_OBJECT = Object.freeze({});

function readYaml(relativePath) {
  return parseYaml(readFileSync(path.join(root, relativePath), "utf8"));
}

function readJson(relativePath) {
  return JSON.parse(readFileSync(path.join(root, relativePath), "utf8"));
}

function assert(condition, message) {
  if (!condition) throw new Error(message);
}

function messageNames(asyncApi) {
  return Object.values(asyncApi.components?.messages ?? EMPTY_OBJECT)
      .map((message) => message?.name)
      .filter((name) => typeof name === "string")
      .sort((left, right) => left.localeCompare(right, "en"));
}

function json(value) {
  return JSON.stringify(value, null, 2);
}

function generate() {
  const openApi = readYaml("spec/openapi/openreceive-http.v1.yaml");
  const asyncApi = readYaml("spec/asyncapi/openreceive-events.v1.yaml");
  const errorSchema = readJson("spec/schemas/error.schema.json");
  const schemas = openApi.components?.schemas ?? EMPTY_OBJECT;
  const checkout = schemas.Checkout;
  const amount = checkout?.properties?.amount_msats;
  const paymentHash = checkout?.properties?.payment_hash;
  const paymentStatuses = schemas.PaymentStatus?.enum ?? [];
  const errorCodes = errorSchema.properties?.code?.enum ?? [];
  const events = messageNames(asyncApi);

  assert(openApi.info?.version, "OpenAPI info.version is required");
  assert(asyncApi.info?.version, "AsyncAPI info.version is required");
  assert(paymentStatuses.length > 0, "OpenAPI payment statuses are required");
  assert(errorCodes.length > 0, "OpenReceive error codes are required");
  assert(events.length > 0, "AsyncAPI event names are required");
  assert(amount?.minimum === 1000, "invoice amount minimum drifted");
  assert(amount?.maximum === 9007199254740991, "invoice amount maximum drifted");
  assert(typeof paymentHash?.pattern === "string", "payment hash pattern is required");

  return `// Generated by tools/codegen/generate-js-models.mjs. Do not edit.

export const OPENRECEIVE_HTTP_CONTRACT_VERSION = ${json(openApi.info.version)} as const;
export const OPENRECEIVE_EVENT_CONTRACT_VERSION = ${json(asyncApi.info.version)} as const;

export const OPENRECEIVE_EVENT_NAMES = ${json(events)} as const;
export type OpenReceiveGeneratedEventName = (typeof OPENRECEIVE_EVENT_NAMES)[number];

export const OPENRECEIVE_ERROR_CODES = ${json(errorCodes)} as const;
export type OpenReceiveGeneratedErrorCode = (typeof OPENRECEIVE_ERROR_CODES)[number];

export const OPENRECEIVE_PAYMENT_STATUSES = ${json(paymentStatuses)} as const;
export type OpenReceiveGeneratedPaymentStatus = (typeof OPENRECEIVE_PAYMENT_STATUSES)[number];

export const OPENRECEIVE_PAYMENT_HASH_PATTERN = ${json(paymentHash.pattern)} as const;

export const OPENRECEIVE_AMOUNT_MSATS_BOUNDARY = {
  minimum: ${amount.minimum},
  maximum: ${amount.maximum}
} as const;
`;
}

const generated = generate();
const absoluteOutputPath = path.join(root, outputPath);

if (check) {
  const current = existsSync(absoluteOutputPath)
    ? readFileSync(absoluteOutputPath, "utf8")
    : "";

  if (current !== generated) {
    console.error(`${outputPath} is stale. Run npm run generate:models.`);
    process.exit(1);
  }

  console.log("Generated model contracts are current.");
} else {
  mkdirSync(path.dirname(absoluteOutputPath), { recursive: true });
  writeFileSync(absoluteOutputPath, generated);
  console.log(`Generated ${outputPath}.`);
}
